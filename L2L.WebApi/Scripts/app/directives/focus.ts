(function () {

    controller.$inject = ["$timeout"];

    function controller($timeout) {
        return {
            scope: {
                trigger: '@focus'
            },
            link: function (scope, element) {
                scope.$watch('trigger', function (value) {
                    if (value === "true") {
                        $timeout(function () {
                            element[0].focus();
                        });
                    }
                });
            }
        };
    }

    l2lApp.directive('focus', controller);
})();
     